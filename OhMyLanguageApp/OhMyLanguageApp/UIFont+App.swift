import UIKit

extension UIFont {
    static func appBody() -> UIFont { .systemFont(ofSize: 15) }
    static func appCaption() -> UIFont { .systemFont(ofSize: 12) }
    static func appCaption2() -> UIFont { .systemFont(ofSize: 11) }
    static func appHeadline() -> UIFont { .systemFont(ofSize: 17) }
    static func appSub() -> UIFont { .systemFont(ofSize: 14) }
}
